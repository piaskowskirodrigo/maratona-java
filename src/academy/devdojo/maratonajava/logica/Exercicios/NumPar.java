package academy.devdojo.maratonajava.logica.Exercicios;

public class NumPar {
    public static void main(String[] args) {
        int num = 2;
        if (num % 2 == 0) {
            System.out.println(num + " é um número par.");
        } else {
            System.out.println(num + " é um número ímpar.");
        }
    }
}
